package com.example.madda219bit0292;

public class Cardview
{
    private int imageView2;
    private  String tv1;
    private  String tv2;
    private  String tv3;

    public Cardview(int imageView2, String tv1, String tv2, String tv3) {
        this.imageView2 = imageView2;
        this.tv1 = tv1;
        this.tv2 = tv2;
        this.tv3 = tv3;
    }

    public int getImageView2() {
        return imageView2;
    }

    public String getTv1() {
        return tv1;
    }

    public String getTv2() {
        return tv2;
    }

    public String getTv3() {
        return tv3;
    }


}
